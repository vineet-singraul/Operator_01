#include <iostream>
using namespace std;
int main()
{
    // Relational operatores
    int a,b;
    cout<<"Enter The Number A : "<<endl;
    cin>>a;
    cout<<"Enter The Number B : "<<endl;
    cin>>b;
    
    cout<<"The Summision Of "<< a << " and " << b<< " Is : "<<a+b<<endl;
    cout<<"The Substraction Of "<< a << " and " << b<< " Is : "<<a-b<<endl;
    cout<<"The Multiplycation Of "<< a << " and " << b<< " Is : "<<a*b<<endl;
    cout<<"The Division Of "<< a << " and " << b<< " Is : "<<a/b<<endl;
    cout<<"The Division Of "<< a << " and " << b<< " Is : "<<a%b<<endl;
}